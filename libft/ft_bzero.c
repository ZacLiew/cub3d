#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	index;

	index = 0;
	if (n == 0)
		return ;
	while (index < n)
	{
		((unsigned char *)s)[index] = '\0';
		index++;
	}
}
